package jayhomeworkweek6;

public class Program18 {

    public static void main(String[] args) {
        calculation(125,24);
    }
//java program to print addition, multiply, subtract, divide and remainder of two number
    public static void calculation(int a, int b) {
        System.out.print("125 + 24 = ");
        System.out.println(a + b);
        System.out.print("125 - 24 = ");
        System.out.println(a - b);
        System.out.print("125 * 24 = ");
        System.out.println(a * b);
        System.out.print("125 / 24 = ");
        System.out.println(a / b);
        System.out.print("125 mod 24 = ");
        System.out.println(a % b);

    }
}


